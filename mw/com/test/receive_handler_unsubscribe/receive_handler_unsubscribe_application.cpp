#include "platform/aas/mw/com/test/receive_handler_unsubscribe/receive_handler_unsubscribe_application.h"

#include "platform/aas/mw/com/impl/instance_specifier.h"
#include "platform/aas/mw/com/test/common_test_resources/sample_sender_receiver.h"
#include "platform/aas/mw/com/test/common_test_resources/sctf_test_runner.h"

#include <future>
#include <utility>
#include <vector>

/**
 * Integration test to test code requirement : "A receive handler that was set by a user shall be no longer
 * invoked after an unsubscribe."
 *
 * This test will create a skeleton and proxy within the same process. The skeleton will publish events and the proxy
 * will receive notifications via a callback registered with SetReceiveHandler(). When Unsubscribe is called on the
 * proxy side, it will be checked that the skeleton publishes at least one more event and the proxy does not call the
 * callback registered to that event.
 */
int main(int argc, const char** argv)
{
    using Parameters = bmw::mw::com::test::SctfTestRunner::RunParameters::Parameters;

    const std::vector<Parameters> allowed_parameters{Parameters::NUM_CYCLES, Parameters::CYCLE_TIME};
    bmw::mw::com::test::SctfTestRunner test_runner(argc, argv, allowed_parameters);
    const auto& run_parameters = test_runner.GetRunParameters();
    const auto num_cycles = run_parameters.GetNumCycles();
    const auto stop_token = test_runner.GetStopToken();
    const auto cycle_time = run_parameters.GetCycleTime();

    bmw::mw::com::test::EventSenderReceiver event_sender_receiver;

    const auto instance_specifier_result = bmw::mw::com::InstanceSpecifier::Create("xpad/cp60/MapApiLanesStamped");
    if (!instance_specifier_result.has_value())
    {
        std::cerr << "Invalid instance specifier, terminating." << std::endl;

        return EXIT_FAILURE;
    }
    const auto& instance_specifier = instance_specifier_result.value();

    std::vector<std::future<int>> future_return_values;
    auto skeleton_return_value = std::async(std::launch::async,
                                            &bmw::mw::com::test::EventSenderReceiver::RunAsSkeleton,
                                            &event_sender_receiver,
                                            instance_specifier,
                                            cycle_time,
                                            num_cycles,
                                            std::cref(stop_token));
    future_return_values.push_back(std::move(skeleton_return_value));

    auto proxy_return_value = std::async(std::launch::async,
                                         &bmw::mw::com::test::EventSenderReceiver::RunAsProxyReceiveHandlerOnly,
                                         &event_sender_receiver,
                                         instance_specifier,
                                         std::cref(stop_token));
    future_return_values.push_back(std::move(proxy_return_value));

    // Wait for all threads to finish and check that they finished safely
    return bmw::mw::com::test::SctfTestRunner::WaitForAsyncTestResults(future_return_values);
}
